    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
            String Idno=jTextField1.getText();
            String oop=jTextField2.getText();
            String dld=jTextField3.getText();
            String phy2=jTextField4.getText();
            String ooplab=jTextField5.getText();
            String dldlab=jTextField6.getText();
            
            try
            {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","rootcode");
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("select *from student where IDNo='"+Idno+"'");
                if(rs.next())
                    {
                        st.executeUpdate("insert into result(IDNo,OOP,DLD,physics2,OOPlab,DLDlab) values('"+Idno+"','"+oop+"','"+dld+"','"+phy2+"','"+ooplab+"','"+dldlab+"')");
                        JOptionPane.showMessageDialog(null,"Successfully updated");
                        setVisible(false);
                        new insertNewResult().setVisible(true);
                    }
                else
                    {
                        JOptionPane.showMessageDialog(null,"Roll number does not exist in our database");
                        setVisible(false);
                        new insertNewResult().setVisible(true);
                    }
            }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Connection error");
        }
            
    }//GEN-LAST:event_jButton6ActionPerformed

